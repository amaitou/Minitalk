#include "minitalk.h"

void	ft_putnbr(int n)
{
	char	a;
	char	b;

	if (n >= 0 && n <= 9)
	{
		a = n + 48 ;
		write(1, &a, 1);
	}
	else if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else if (n < 0)
	{
		b = '-';
		write(1, &b, 1);
		ft_putnbr(n * (-1));
	}
	else
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
	}
}
